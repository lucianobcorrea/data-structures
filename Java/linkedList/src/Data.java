
public class Data {
	
	private Integer dia;
	private Integer mes;
	private Integer ano;
	private Data proximo;
	
	public int getDia() {
		return dia;
	}
	
	public void setDia(Integer dia) {
		this.dia = dia;
	}
	
	public int getMes() {
		return mes;
	}
	
	public void setMes(Integer mes) {
		this.mes = mes;
	}
	
	public int getAno() {
		return ano;
	}
	
	public void setAno(Integer ano) {
		this.ano = ano;
	}
	
	public Data getProximo() {
		return proximo;
	}
	
	public void setProximo(Data proximo) {
		this.proximo = proximo;
	}	
	
}
